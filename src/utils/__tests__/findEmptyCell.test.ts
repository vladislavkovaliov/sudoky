import { describe, test, expect } from "vitest";
import { findEmptyCell } from "../findEmptyCell";
import { IGrid } from "../../types";

describe("[findEmptyCell.ts]", () => {
  test("should return null value if the cell not is empty", () => {
    const mockGrid: IGrid = [
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
    ];

    const result = findEmptyCell(mockGrid);

    expect(result).toBe(null);
  });

  test("should return object with fields row and col if the cell has null value", () => {
    const mockGrid: IGrid = [
      [null, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
      [1, 2, 3, 4, 5, 6, 7, 8, 9],
    ];

    const result = findEmptyCell(mockGrid);
    const expectedResult = { row: 0, column: 0 };

    expect(result).toStrictEqual(expectedResult);
  });
});
